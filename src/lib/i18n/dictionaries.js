// Bilingual dictionaries for Sarayah. English is the default; Arabic is a toggle
// with full RTL. Plain JS so this module is safe to import from BOTH server
// components (via lib/i18n/server.js) and client components (via the provider in
// lib/i18n/client.js) — keep it free of any server-only APIs.
//
// `enums` translate stored data values (venue type, setting, amenities, event
// type) so the Arabic UI reads as real Arabic instead of half-English. Use the
// `tv(category, value)` helper (exposed by getI18n / useI18n) to read them; it
// falls back to the raw value when a translation is missing.

export const LOCALES = ["en", "ar"];
export const DEFAULT_LOCALE = "en";
export const LOCALE_COOKIE = "sarayah_locale";

export function dirFor(locale) {
  return locale === "ar" ? "rtl" : "ltr";
}

export function normalizeLocale(value) {
  return LOCALES.includes(value) ? value : DEFAULT_LOCALE;
}

// translateEnum(t, "type", "Hotel") -> "فندق" (or "Hotel" if not mapped).
export function translateEnum(t, category, value) {
  if (value == null) return value;
  return t?.enums?.[category]?.[value] ?? value;
}

